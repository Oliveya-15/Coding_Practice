#Check whether a given number is even or odd
n=int(input("Enter the Number: "))
if n%2 == 0:
    print("The Number is Even: ",n)
else:
    print("The Number is Odd: ",n)

# Input: n=5
# Output: odd
# Explanation: 5 is not divisible by 2.
 
