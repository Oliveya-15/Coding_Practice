#Check whether a number is positive or negative
n=int(input("Enter the Number: "))
if n>0:
    print("The Number is Positive: ",n)
elif n<0:
    print("The Number is Negative: ",n)
elif n==0:
    print("The Number is Zero: ",n)
else:
    print("Not a Number")



# Input: n=5
# Output: Positive

